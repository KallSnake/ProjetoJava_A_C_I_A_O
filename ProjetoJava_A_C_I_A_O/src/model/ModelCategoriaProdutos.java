package model;

public class ModelCategoriaProdutos {
    
    private Integer cod_catprod;
    private String nome_catprod;
    private String pesquisa;
    
    

    public Integer getCod_catprod() {
        return cod_catprod;
    }

    public void setCod_catprod(Integer cod_catprod) {
        this.cod_catprod = cod_catprod;
    }

    public String getNome_catprod() {
        return nome_catprod;
    }

    public void setNome_catprod(String nome_catprod) {
        this.nome_catprod = nome_catprod;
    }

    public String getPesquisa() {
        return pesquisa;
    }

    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }

    
    
}
